#include <stdio.h>

/**
* main - prints "$ ", waits for the user to enter command, and prints it on the next line
* @ac: argument count
* @av: argument vector
*
* Return: 0
*/

int main(int ac, char **av)
{
	char lineptr[100];
	void(ac);

	printf("$ ");
	scanf("%s", lineptr[100]);
	printf("\n%s\n", av[0]);

	return (0);
}
